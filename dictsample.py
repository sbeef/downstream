# a dictionary implementation of dsample

def get_name(sample):
    sample['name']

def get_latitude(sample):
    sample['latitude']

def get_longitude(sample):
    sample['longitude']

def get_watershed(sample):
    sample['watershed']

def set_name(sample, name):
    sample['name'] = name

def set_longitude(sample, longitude):
    sample['longitude'] = longitude

def set_latitude(sample, latitude):
    sample['latitude'] = latitude

def set_watershed(sample, watershed):
    sample['watershed'] = watershed


